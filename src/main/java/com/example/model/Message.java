package com.example.model;

public class Message {
	private int MseId;
	private String MesName;
	private String MesEmail;
	private String Mestext;
	public int getMseId() {
		return MseId;
	}
	public void setMseId(int mseId) {
		MseId = mseId;
	}
	public String getMesName() {
		return MesName;
	}
	public void setMesName(String mesName) {
		MesName = mesName;
	}
	public String getMesEmail() {
		return MesEmail;
	}
	public void setMesEmail(String mesEmail) {
		MesEmail = mesEmail;
	}
	public String getMestext() {
		return Mestext;
	}
	public void setMestext(String mestext) {
		Mestext = mestext;
	}
	
}
